int main() {
    // 必须的IO加速
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n; // 游戏总轮次数
    cin >> n;

    for (int i = 0; i < n; ++i) {
        int val1, val2, val3;
        cin >> val1 >> val2 >> val3;

        // DEBUG: 打印当前轮次的盒子值
        // cout << "// DEBUG: Round " << i+1 << " boxes: " << val1 << ", " << val2 << ", " << val3 << endl;

        // 核心逻辑：Alice 选择当前轮次中价值最高的盒子
        // 证明过程:
        // 令 s1 < s2 < s3 为排序后的盒子价值
        // P_bob(s1)=1/5, P_bob(s2)=2/5, P_bob(s3)=2/5
        // E_diff(Alice_chooses_s1) = (1/5)*0 + (2/5)*(s1-s2) + (2/5)*(s1-s3) = (4s1 - 2s2 - 2s3)/5
        // E_diff(Alice_chooses_s2) = (1/5)*(s2-s1) + (2/5)*0 + (2/5)*(s2-s3) = (-s1 + 3s2 - 2s3)/5
        // E_diff(Alice_chooses_s3) = (1/5)*(s3-s1) + (2/5)*(s3-s2) + (2/5)*0 = (-s1 - 2s2 + 3s3)/5
        // 比较可知 E_diff(Alice_chooses_s3) 是最大的。

        int alice_choice = val1;
        if (val2 > alice_choice) {
            alice_choice = val2;
        }
        if (val3 > alice_choice) {
            alice_choice = val3;
        }
        // 或者使用 std::max (更简洁)
        // int alice_choice = std::max({val1, val2, val3}); // C++11 initializer list for std::max

        cout << alice_choice << "\n";
    }

    return 0;
}